    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        //add button
        try {
            // Validate and get common fields
            int hours = validateIntegerInput(jTextField1.getText(), "Hours");
            String name = jTextField2.getText().trim();
            
            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Name cannot be empty.", "Input Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            Staff newStaff = null;
            
            // Create appropriate staff object based on selected type
            if (jRadioButton1.isSelected()) {
                // Manager
                int officeNumber = validateIntegerInput(jTextField3.getText(), "Office Number");
                newStaff = new Manager(hours, name, officeNumber);
            } 
            else if (jRadioButton2.isSelected()) {
                // Artist
                int yearsOfExperience = validateIntegerInput(jTextField4.getText(), "Years of Experience");
                newStaff = new Artist(hours, name, yearsOfExperience);
            } 
            else if (jRadioButton3.isSelected()) {
                // Inspector
                int yearsOfExperience = validateIntegerInput(jTextField4.getText(), "Years of Experience");
                int inspections = validateIntegerInput(jTextField5.getText(), "Number of Inspections");
                newStaff = new Inspector(hours, name, yearsOfExperience, inspections);
            }
            
            // Add staff to museum
            if (newStaff != null) {
                if (TestMuseum.m1.addStaff(newStaff)) {
                    JOptionPane.showMessageDialog(this, "Staff member added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                    clearFields();
                } else {
                    JOptionPane.showMessageDialog(this, "Could not add staff. The museum is full.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (NumberFormatException e) {
            // Error message shown by validateIntegerInput method
        }
    }   
