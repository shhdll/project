private int validateIntegerInput(String input, String fieldName) throws NumberFormatException {
        try {
            return Integer.parseInt(input.trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, 
                "Please enter a valid integer for " + fieldName + ".", 
                "Input Error", 
                JOptionPane.ERROR_MESSAGE);
            throw e;
        }
    }
