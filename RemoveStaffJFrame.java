    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
          String staffName = jTextField1.getText().trim();

        if (staffName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a staff name.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Staff staffToRemove = TestMuseum.m1.searchStaff(staffName);

        if (staffToRemove != null) {
            boolean removed = TestMuseum.m1.removeStaff(staffToRemove);
            if (removed) {
                JOptionPane.showMessageDialog(this, "Staff removed successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Failed to remove staff.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Staff not found.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }                                        

